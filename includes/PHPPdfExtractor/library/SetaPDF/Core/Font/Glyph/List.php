    /**
     * Get the UTF-16BE values by a glyph name.
     * 
     * @param string $name The glyph name
     * @param string $primaryList Use LIST_XXX constant
     * @return bool|string If glyph was found the UTF-16BE values will be returned as <b>string</b>.<br/>
     *                     Otherwise <b>false</b> will be returned.<br/>
     * @throws InvalidArgumentException if the glyph list is unknown.
     */
    static public function byName($name, $primaryList = self::LIST_AGL)
    {
        if (($pos = strpos($name, '.')) !== false) {
            $name = substr($name, 0, $pos);
        }

        $result = '';
        foreach (explode('_', $name) AS $_name) {
            $result .= self::_byName($_name, $primaryList);
        }

        return $result;
    }

    /**
     * Get the UTF-16BE value from a glyph list.
     *
     * @param $name
     * @param string $primaryList
     * @return string
     */
    static protected function _byName($name, $primaryList = self::LIST_AGL)
    {
        if ($name === '') {
            return '';
        }

        if (!isset(self::$lists[$primaryList])) {
            throw new InvalidArgumentException(sprintf('Unknown glyph list: %s', $primaryList));
        }

        if (isset(self::$lists[$primaryList][$name])) {
            return self::$lists[$primaryList][$name];
        }

        // fallback
        if ($primaryList !== self::LIST_AGL && isset(self::$lists[self::LIST_AGL][$name])) {
            return self::$lists[self::LIST_AGL][$name];
        }

        if (strpos($name, 'u') === 0) {
            if (strpos($name, 'ni') === 1) {
                $hex = substr($name, 3);
                if (strtoupper($hex) !== $hex) {
                    return '';
                }

                return SetaPDF_Core_Type_HexString::hex2str($hex);
            }

            $hex = substr($name, 1);
            if (strtoupper($hex) === $hex && ctype_xdigit($hex)) {
                $length = strlen($hex);
                if ($length < 4 || $length > 6) {
                    return '';
                }

                return SetaPDF_Core_Encoding::unicodePointToUtf16Be(base_convert($hex, 16, 10));
            }
        }

        if (null !== self::$lists[self::LIST_CUSTOM]) {
            if (is_callable(self::$lists[self::LIST_CUSTOM])) {
                return call_user_func(self::$lists[self::LIST_CUSTOM], $name);
            } elseif (is_array(self::$lists[self::LIST_CUSTOM]) && isset(self::$lists[self::LIST_CUSTOM][$name])) {
                return self::$lists[self::LIST_CUSTOM][$name];
            }
        }

        return '';
    }
    
    /**
     * Get the glyph name by the UTF-16BE value.
     * 
     * @param string $code The UTF-16BE value
     * @param string $primaryList Use LIST_XXX constant
     * @return string The glyph name will be returned as <b>string</b>.
     * @throws InvalidArgumentException if the glyph list is unknown.
     */
    static public function byUtf16Be($code, $primaryList = self::LIST_AGL)
    {
        if (!isset(self::$lists[$primaryList])) {
            throw new InvalidArgumentException(sprintf('Unknown glyph list: %s', $primaryList));
        }
        
        $glyph = array_search($code, self::$lists[$primaryList]);

        if (false !== $glyph) {
            return $glyph;
        }

        if (SetaPDF_Core_Encoding::strlen($code, 'UTF-16BE') == 0) {
            return '';
        }

        return 'uni' . strtoupper(SetaPDF_Core_Type_HexString::str2hex($code));
    }

    /**
     * Get a list of all glyphs used in the string.
     *
     * @param string $string The UTF-16BE value
     * @param string $primaryList Use LIST_XXX constant
     * @return array
     * @throws InvalidArgumentException if the glyph list is unknown.
     */
    static public function byUtf16BeString($string, $primaryList = self::LIST_AGL)
    {
        $names = array();
        $len = SetaPDF_Core_Encoding::strlen($string, 'UTF-16BE');
        for ($i = 0; $i < $len; $i++) {
            $code = SetaPDF_Core_Encoding::substr($string, $i, 1, 'UTF-16BE');
            $names[$code] = self::byUtf16Be($code, $primaryList);
        }
            
        return $names;
    }

    /**
     * Get the glyph name by the value.
     *
     * @param string $code The value
     * @param string $encoding The encoding of the value
     * @param string $primaryList Use LIST_XXX constant
     * @return bool|string If glyph was found the name will be returned as <b>string</b>.<br/>
     *                     Otherwise <b>false</b> will be returned.<br/>
     * @throws InvalidArgumentException if the glyph list is unknown.
     */
    static public function byCode($code, $encoding = 'UTF-16BE', $primaryList = self::LIST_AGL)
    {
        if ($encoding !== 'UTF-16BE') {
            $code = SetaPDF_Core_Encoding::convert($code, $encoding, 'UTF-16BE');
        }
        
        return self::byUtf16Be($code, $primaryList);
    }
    
